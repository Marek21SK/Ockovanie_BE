package MDMMDM.demo;

import java.util.Date;

public class OsobaDto {
    private Long id;
    private String meno;
    private String priezvisko;
    private String email;
    private Date datum_narodenia;
    private String kontakt;
    private String pohlavie;
    private String bydlisko;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getMeno() {
        return meno;
    }

    public void setMeno(String meno) {
        this.meno = meno;
    }

    public String getPriezvisko() {
        return priezvisko;
    }

    public void setPriezvisko(String priezvisko) {
        this.priezvisko = priezvisko;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getDatum_narodenia() {
        return datum_narodenia;
    }

    public void setDatum_narodenia(Date datum_narodenia) {
        this.datum_narodenia = datum_narodenia;
    }

    public String getKontakt() {
        return kontakt;
    }

    public void setKontakt(String kontakt) {
        this.kontakt = kontakt;
    }

    public String getPohlavie() {
        return pohlavie;
    }

    public void setPohlavie(String pohlavie) {
        this.pohlavie = pohlavie;
    }

    public String getBydlisko() {
        return bydlisko;
    }

    public void setBydlisko(String bydlisko) {
        this.bydlisko = bydlisko;
    }
}
